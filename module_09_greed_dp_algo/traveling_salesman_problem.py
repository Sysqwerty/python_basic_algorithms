# Задача комівояжера (TSP – Traveling Salesman Problem) — це класична задача оптимізації, яка полягає в знаходженні
# найкоротшого можливого маршруту, за яким можна відвідати кожне місто лише один раз і повернутися до початкового
# міста. Це NP-повна задача, що означає, що час знаходження точного рішення зростає експоненційно зі збільшенням
# кількості міст.
